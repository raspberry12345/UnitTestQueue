public class QueueItem {


    private int data;
    private QueueItem next;


    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public QueueItem getNext() {
        return next;
    }

    public void setNext(QueueItem next) {
        this.next = next;
    }


}
